# 运算符
# 除 / ，结果是浮点数float
print(1 / 1)
# 除取整 //
print(3 // 2)
# 除取余数 %
print(8 % 3)
# 幂 ** ，m**n：m的n次方
print(2 ** 3)
# 算数运算符有浮点数，计算结果为浮点数
print(2.0 * 3)
print(2.0 + 3)

# 赋值运算符 =
num1 = 5
num2 = num1
print(num2)
num3 = num1 + num2
print(num3)
num3 += 3
print(num3)

# 输入函数 input，  参数prompt：输入时的提示
s = input("请输入")
print(s)
# 输入类型控制 int float
i = int(input("请输入数字"))
print(i)
